#!/usr/bin/env node

'use strict';

// CODEOWNERS generator (Increment 2, C3). Renders .github/CODEOWNERS from
// project-manifest.json#github so the ruleset's require_code_owner_review rule is
// not inert. Config-driven and literal-free: every owner comes from
// github.default_owners (the `* <owners>` catch-all) and the optional
// github.path_owners map (per-path lines). Idempotent — the same config always
// renders byte-identical output. When default_owners is empty it renders nothing
// and writes no file (a CODEOWNERS with no owners is worse than none).

const fs = require('fs');
const path = require('path');

const HEADER = [
  '# Generated by generate-codeowners.js from project-manifest.json#github.',
  '# Do not hand-edit — change github.default_owners / github.path_owners and regenerate.',
];

// An owner is a @user, @org/team, or an email. A path is any non-whitespace,
// non-comment token. Both are validated to reject a newline (or other whitespace)
// smuggling extra CODEOWNERS rules in — e.g. default_owners:['@t\n* @attacker']
// would otherwise render a second global-owner line the wiring gate can't see.
const OWNER_RE = /^(@[A-Za-z0-9-]+(\/[A-Za-z0-9._-]+)?|[^@\s]+@[^@\s]+)$/;
const PATH_RE = /^[^\s#]+$/;

function assertOwner(o) {
  if (typeof o !== 'string' || !OWNER_RE.test(o)) {
    throw new Error(`invalid CODEOWNERS owner ${JSON.stringify(o)} — must be @user, @org/team, or an email, with no whitespace/newline`);
  }
}

// Returns the CODEOWNERS text, or null when there are no default owners to assign.
function renderCodeowners(github) {
  const owners = (github && Array.isArray(github.default_owners)) ? github.default_owners : [];
  if (owners.length === 0) return null;
  owners.forEach(assertOwner);
  const lines = [...HEADER, '', `* ${owners.join(' ')}`];
  const pathOwners = (github && github.path_owners) || {};
  for (const p of Object.keys(pathOwners)) {
    if (!PATH_RE.test(p)) {
      throw new Error(`invalid CODEOWNERS path ${JSON.stringify(p)} — no whitespace, newline, or '#'`);
    }
    const who = Array.isArray(pathOwners[p]) ? pathOwners[p] : [];
    who.forEach(assertOwner);
    if (who.length) lines.push(`${p} ${who.join(' ')}`);
  }
  return lines.join('\n') + '\n';
}

// Writes .github/CODEOWNERS under target, or skips (returns null) when empty.
function writeCodeowners(target, github) {
  const text = renderCodeowners(github);
  if (text === null) return null;
  const out = path.join(target, '.github', 'CODEOWNERS');
  fs.mkdirSync(path.dirname(out), { recursive: true });
  fs.writeFileSync(out, text);
  return out;
}

function readGithub(cwd) {
  try {
    const m = JSON.parse(fs.readFileSync(path.join(cwd, 'project-manifest.json'), 'utf8'));
    return (m && m.github) || null;
  } catch (_) { return null; }
}

function run(cwd = process.cwd()) {
  const github = readGithub(cwd);
  let out;
  try {
    out = writeCodeowners(cwd, github || {});
  } catch (err) {
    process.stderr.write(`generate-codeowners: ${String((err && err.message) || err)}\n`);
    return 1;
  }
  if (out) process.stdout.write(`generate-codeowners: wrote ${path.relative(cwd, out)}\n`);
  else process.stdout.write('generate-codeowners: github.default_owners is empty — no CODEOWNERS written.\n');
  return 0;
}

module.exports = { renderCodeowners, writeCodeowners };

if (require.main === module) process.exit(run());
